async function getDeploymentBlock(
  tx: {
    wait: (confirmations?: number) => Promise<{ blockNumber: number }>;
  } | null,
): Promise<number> {
  try {
    if (!tx) return await ethers.provider.getBlockNumber();
    const receipt = await tx.wait(1);
    return receipt.blockNumber;
  } catch {
    return await ethers.provider.getBlockNumber();
  }
}

async function waitForConfirmations(
  tx: { wait: (confirmations?: number) => Promise<unknown> } | null,
  confirmations: number = 2,
): Promise<void> {
  try {
    if (!tx) return;
    console.log(`  Waiting for ${confirmations} confirmations...`);
    await tx.wait(confirmations);
    console.log('  ✓ Confirmed');
  } catch {
    console.warn('  Warning: Could not wait for all confirmations');
  }
}

async function resolveActiveDiamondAddress(): Promise<string> {
  const chainId = Number((await ethers.provider.getNetwork()).chainId);
  const manifest = loadDeploymentManifest({
    deploymentsDir: path.resolve(process.cwd(), 'deployments'),
    networkName: network.name,
    chainId,
  });
  const chainConstantsDiamondAddress = readDiamondAddressFromChainConstants(
    path.resolve(process.cwd(), 'chain-constants.ts'),
  );
  const diamondAddress = resolveRuntimeDiamondAddress({
    manifestDiamondAddress: manifest?.diamond,
    chainConstantsDiamondAddress,
    preferManifestOverChainConstants: true,
    env: process.env,
  });
  const code = await ethers.provider.getCode(diamondAddress);

  if (!code || code === '0x') {
    throw new Error(
      `Resolved Diamond address ${diamondAddress} has no code on ${network.name} (${chainId}). Provide DIAMOND_ADDRESS or ensure deployments/manifest.${network.name}.json is available.`,
    );
  }

  return diamondAddress;
}

/**
 * Extract function selectors directly from a compiled contract
 * This is used when FACET_ABI is not defined for a facet
 */
async function extractSelectorsFromContract(
  contractName: string,
): Promise<string[]> {
  try {
    const factory = await ethers.getContractFactory(contractName);
    const contractInterface = factory.interface;

    // Get all function fragments and extract their selectors
    const selectors: string[] = [];
    for (const fragment of contractInterface.fragments) {
      if (fragment.type === 'function') {
        const funcFragment = fragment as ethers.FunctionFragment;
        if (funcFragment.selector) {
          selectors.push(funcFragment.selector);
        }
      }
    }

    return selectors;
  } catch (error: any) {
    console.error(
      `   ⚠️  Could not extract selectors from ${contractName}: ${error.message}`,
    );
    return [];
  }
}

function shouldAutoWaitForDiamondCutTimelock(): boolean {
  return process.env.DEPLOY_AUTO_WAIT_FOR_DIAMOND_CUT === 'true';
}

function sleep(ms: number): Promise<void> {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function computeDiamondCutHash(
  facetCuts: FacetCutInput[],
  init: string,
  calldata: string,
): string {
  const abiCoder = ethers.AbiCoder.defaultAbiCoder();
  return ethers.keccak256(
    abiCoder.encode(
      [
        'tuple(address facetAddress, uint8 action, bytes4[] functionSelectors)[]',
        'address',
        'bytes',
      ],
      [facetCuts, init, calldata],
    ),
  );
}

async function executeDiamondCutWithScheduling(
  diamondAddress: string,
  facetCuts: FacetCutInput[],
  init: string = ethers.ZeroAddress,
  calldata: string = '0x',
): Promise<void> {
  const [signer] = await ethers.getSigners();
  const diamondCut = new ethers.Contract(
    diamondAddress,
    DIAMOND_CUT_EXTENDED_ABI,
    signer,
  );
  const getPendingNonce = () => signer.getNonce('pending');

  const timelock = BigInt(await diamondCut.getDiamondCutTimelock());
  if (timelock === 0n) {
    const tx = await sendWithNonceRetry({
      label: 'diamondCut',
      getPendingNonce,
      logger: console,
      send: (overrides) =>
        diamondCut.diamondCut(facetCuts, init, calldata, overrides ?? {}),
    });
    await tx.wait();
    return;
  }

  const expectedCutHash = computeDiamondCutHash(facetCuts, init, calldata);
  let [pendingHash, pendingEta] = await diamondCut.getPendingDiamondCut();
  let pendingHashStr = String(pendingHash).toLowerCase();
  let pendingEtaBigInt = BigInt(pendingEta);

  if (pendingEtaBigInt === 0n) {
    console.log('   Scheduling diamond cut (timelock enabled)...');
    const scheduleTx = await sendWithNonceRetry({
      label: 'scheduleDiamondCut',
      getPendingNonce,
      logger: console,
      send: (overrides) =>
        diamondCut.scheduleDiamondCut(
          facetCuts,
          init,
          calldata,
          overrides ?? {},
        ),
    });
    await scheduleTx.wait();
    [pendingHash, pendingEta] = await diamondCut.getPendingDiamondCut();
    pendingHashStr = String(pendingHash).toLowerCase();
    pendingEtaBigInt = BigInt(pendingEta);
  }

  if (pendingHashStr !== expectedCutHash.toLowerCase()) {
    throw new Error(
      `Pending diamond cut does not match this deployment (pending=${pendingHashStr}, expected=${expectedCutHash}).`,
    );
  }

  const now = BigInt(Math.floor(Date.now() / 1000));
  if (pendingEtaBigInt > now) {
    const secondsRemaining = Number(pendingEtaBigInt - now);
    if (!shouldAutoWaitForDiamondCutTimelock()) {
      throw new Error(
        `Diamond cut scheduled but timelock is still active for ~${secondsRemaining}s. Set DEPLOY_AUTO_WAIT_FOR_DIAMOND_CUT=true to wait and execute automatically.`,
      );
    }

    console.log(
      `   Timelock active. Waiting ~${secondsRemaining}s before executing diamond cut...`,
    );
    while (true) {
      const current = Math.floor(Date.now() / 1000);
      const remaining = Number(pendingEtaBigInt) - current;
      if (remaining <= 0) break;
      await sleep(Math.min(remaining, 30) * 1000);
    }
  }

  console.log('   Executing scheduled diamond cut...');
  const executeTx = await sendWithNonceRetry({
    label: 'diamondCut',
    getPendingNonce,
    logger: console,
    send: (overrides) =>
      diamondCut.diamondCut(facetCuts, init, calldata, overrides ?? {}),
  });
  await executeTx.wait();
}

