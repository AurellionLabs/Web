interface FacetSelectorsData {
  [facetName: string]: {
    selectors: { [functionName: string]: string };
    allSelectors: string[];
  };
}

const FACET_SELECTORS_PATH = path.join(
  __dirname,
  '../infrastructure/contracts/facet-selectors.generated.json',
);
const DEFAULT_SUPPORTED_ASSET_CLASSES = getSupportedAssetClasses(
  loadSupportedAssetCatalog(),
);

function loadFacetSelectors(): Record<string, string[]> {
  if (!fs.existsSync(FACET_SELECTORS_PATH)) {
    console.warn(
      '⚠️  facet-selectors.json not found. Run: npm run contract:gen',
    );
    return {};
  }

  const data: FacetSelectorsData = JSON.parse(
    fs.readFileSync(FACET_SELECTORS_PATH, 'utf8'),
  );

  // Convert to the legacy format (facetName -> array of selectors)
  const selectors: Record<string, string[]> = {};
  for (const [facetName, facetData] of Object.entries(data)) {
    selectors[facetName] = facetData.allSelectors;
  }

  return selectors;
}

// Load selectors at module initialization
const FACET_SELECTORS = loadFacetSelectors();

