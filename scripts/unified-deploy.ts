async function deployContract(
  config: ContractConfig,
  addresses: Record<string, string>,
  deployer: string,
): Promise<{ address: string; blockNumber: number }> {
  console.log(`\n📦 Deploying ${config.name}...`);

  const Factory = await ethers.getContractFactory(config.contractName);
  const [signer] = await ethers.getSigners();

  // Get constructor args
  const args = config.constructorArgs
    ? config.constructorArgs(addresses, deployer)
    : [];

  if (args.length > 0) {
    console.log(`   Constructor args: ${JSON.stringify(args)}`);
  }

  const contract = await sendWithNonceRetry({
    label: `deploy ${config.name}`,
    getPendingNonce: () => signer.getNonce('pending'),
    logger: console,
    send: (overrides) => Factory.deploy(...args, overrides ?? {}),
  });
  await waitForConfirmations(contract.deploymentTransaction());

  const address = await contract.getAddress();
  const blockNumber = await getDeploymentBlock(
    contract.deploymentTransaction(),
  );

  console.log(`   ✓ ${config.name}: ${address} (block ${blockNumber})`);

  // Run post-deploy hook if exists
  if (config.postDeploy) {
    await config.postDeploy(contract, addresses);
  }

  return { address, blockNumber };
}

async function deployMode(
  modeName: string,
  dryRun: boolean = false,
): Promise<DeploymentResult> {
  const mode = DEPLOYMENT_MODES[modeName];
  if (!mode) {
    throw new Error(
      `Unknown deployment mode: ${modeName}. Use --list-modes to see available modes.`,
    );
  }

  console.log(`\n🚀 ${mode.name}`);
  console.log(`   ${mode.description}`);
  console.log(`   Contracts: ${mode.contracts.join(', ')}\n`);

  if (dryRun) {
    console.log('🔍 DRY RUN - No contracts will be deployed\n');
    return {
      network: network.name,
      chainId: 0,
      deployer: '',
      contracts: {},
      timestamp: new Date().toISOString(),
    };
  }

  const [deployer] = await ethers.getSigners();
  const chainId = Number((await ethers.provider.getNetwork()).chainId);

  console.log(`Network: ${network.name} (Chain ID: ${chainId})`);
  console.log(`Deployer: ${deployer.address}`);
  console.log(
    `Balance: ${ethers.formatEther(await deployer.provider.getBalance(deployer.address))} ETH\n`,
  );

  const addresses: Record<string, string> = {};
  const deployedContracts: Record<
    string,
    { address: string; blockNumber: number }
  > = {};

  // Deploy contracts in order (respecting dependencies)
  for (const contractName of mode.contracts) {
    const config = CONTRACTS[contractName];
    if (!config) {
      console.warn(`⚠️  Unknown contract: ${contractName}, skipping`);
      continue;
    }

    // Check dependencies
    if (config.dependencies) {
      for (const dep of config.dependencies) {
        if (!addresses[dep]) {
          throw new Error(
            `Dependency ${dep} not deployed yet for ${contractName}`,
          );
        }
      }
    }

    const result = await deployContract(config, addresses, deployer.address);
    addresses[contractName] = result.address;
    deployedContracts[contractName] = result;

    // Small delay between deployments to prevent nonce race conditions on fast networks (Arbitrum, etc.)
    await new Promise((resolve) => setTimeout(resolve, 2000));
  }

  // Handle Diamond facet installation if Diamond was deployed
  if (deployedContracts.Diamond && modeName.includes('diamond')) {
    await installFacetsToDiamond(addresses, deployedContracts, mode.contracts);
  }

  // Post-deployment configuration
  await postDeploymentConfig(addresses, deployer.address);

  // Generate indexer from new contract ABIs (if deploying facets)
  const deployedFacets = mode.contracts.filter((name) =>
    name.endsWith('Facet'),
  );
  if (deployedFacets.length > 0 && !dryRun) {
    runIndexerGeneration();
  }

  return {
    network: network.name,
    chainId,
    deployer: deployer.address,
    contracts: deployedContracts,
    timestamp: new Date().toISOString(),
  };
}

async function installFacetsToDiamond(
  addresses: Record<string, string>,
  deployedContracts: Record<string, { address: string; blockNumber: number }>,
  modeContracts: string[],
) {
  console.log('\n⚙️  Installing facets to Diamond...\n');
  void deployedContracts;

  // Derive facets to install from the deployment mode's contract list.
  // This is the single source of truth - adding a facet to a deployment mode
  // automatically includes it in the Diamond cut. No hardcoded lists to maintain.
  // DiamondCutFacet is excluded because it's already added in the Diamond constructor.
  const CONSTRUCTOR_FACETS = ['DiamondCutFacet'];
  const facetsToInstall = modeContracts.filter(
    (name) =>
      CONTRACTS[name]?.category === 'facet' &&
      !CONSTRUCTOR_FACETS.includes(name),
  );

  console.log(`   Facets to install: ${facetsToInstall.join(', ')}\n`);

  const diamondLoupe = await ethers.getContractAt(
    'IDiamondLoupe',
    addresses.Diamond,
  );
  const activeSelectors = new Set<string>();
  const facets = await diamondLoupe.facets();

  for (const facet of facets) {
    for (const selector of facet.functionSelectors) {
      activeSelectors.add(selector.toLowerCase());
    }
  }

  for (const facetName of facetsToInstall) {
    if (!addresses[facetName] || !FACET_SELECTORS[facetName]) continue;

    console.log(`   Adding ${facetName}...`);
    const rawSelectors = FACET_SELECTORS[facetName];
    const { selectorsToAdd, duplicateCount, alreadyInstalledCount } =
      planFacetSelectorAdditions({
        selectors: rawSelectors,
        activeSelectors,
      });

    if (duplicateCount > 0) {
      console.log(
        `   ⚠️  Removed ${duplicateCount} duplicate selector(s) from ${facetName}`,
      );
    }

    if (alreadyInstalledCount > 0) {
      console.log(
        `   ⚠️  Skipping ${alreadyInstalledCount} selector(s) already installed on the Diamond`,
      );
    }

    if (selectorsToAdd.length === 0) {
      console.log(`   ✓ No new selectors to add for ${facetName}`);
      continue;
    }

    try {
      await executeDiamondCutWithScheduling(addresses.Diamond, [
        {
          facetAddress: addresses[facetName],
          action: 0, // Add
          functionSelectors: selectorsToAdd,
        },
      ]);
      selectorsToAdd.forEach((selector) =>
        activeSelectors.add(selector.toLowerCase()),
      );
      console.log(`   ✓ Added ${selectorsToAdd.length} selectors`);
    } catch (error: any) {
      throw new Error(`Failed to install ${facetName}: ${error.message}`);
    }
  }
}

